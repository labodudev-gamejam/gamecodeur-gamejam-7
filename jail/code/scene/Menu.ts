class MenuScene extends Scene {
  constructor() {
    super();

    this.Start();
  }

  public Start():void {
    super.Start();
  }

  public Update(deltaTime: number):void {
    super.Update(deltaTime);
  }

  public Draw(context: any):void {
    super.Draw(context);
  }

	public Clear():void {
    super.Clear();
	}

  public ChangeScene():void {
    this.Clear();
  }
}
